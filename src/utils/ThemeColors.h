#pragma once

#include <QColor>

struct ThemeIconColors {
    QColor normal;
    QColor active;
    QColor disabled;
};

struct ThemeColors {
    ThemeIconColors titleBar;
    ThemeIconColors sidebar;
    ThemeIconColors modeSwitch;
    ThemeIconColors bottomBar;
    ThemeIconColors placeholder;
    QColor bottomText;

    static ThemeColors light();
    static ThemeColors dark();
    static const ThemeColors &forMode(bool darkMode);
};

inline ThemeColors ThemeColors::light()
{
    return {
        { QColor(0x26, 0x33, 0x4b), QColor(0x09, 0x67, 0xf2), QColor(0xa0, 0xaa, 0xb8) },
        { QColor(0x17, 0x20, 0x33), QColor(0x09, 0x67, 0xf2), QColor(0xa0, 0xaa, 0xb8) },
        { QColor(0x1f, 0x29, 0x40), QColor(0x09, 0x67, 0xf2), QColor(0xa0, 0xaa, 0xb8) },
        { QColor(0x53, 0x61, 0x7a), QColor(0x09, 0x67, 0xf2), QColor(0xa0, 0xaa, 0xb8) },
        { QColor(0x64, 0x70, 0x8a), QColor(0x09, 0x67, 0xf2), QColor(0xa0, 0xaa, 0xb8) },
        QColor(0x53, 0x61, 0x7a)
    };
}

inline ThemeColors ThemeColors::dark()
{
    return {
        { QColor(0x9a, 0xa8, 0xbc), QColor(0x4d, 0xa3, 0xff), QColor(0x50, 0x58, 0x68) },
        { QColor(0xc8, 0xd0, 0xdc), QColor(0x4d, 0xa3, 0xff), QColor(0x50, 0x58, 0x68) },
        { QColor(0xb0, 0xbc, 0xc8), QColor(0x4d, 0xa3, 0xff), QColor(0x50, 0x58, 0x68) },
        { QColor(0x7a, 0x8a, 0xa0), QColor(0x4d, 0xa3, 0xff), QColor(0x50, 0x58, 0x68) },
        { QColor(0x9a, 0xa8, 0xbc), QColor(0x4d, 0xa3, 0xff), QColor(0x50, 0x58, 0x68) },
        QColor(0x7a, 0x8a, 0xa0)
    };
}

inline const ThemeColors &ThemeColors::forMode(bool darkMode)
{
    static const ThemeColors lightInstance = light();
    static const ThemeColors darkInstance = dark();
    return darkMode ? darkInstance : lightInstance;
}
